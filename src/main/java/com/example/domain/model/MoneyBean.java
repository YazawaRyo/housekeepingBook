package com.example.domain.model;

public class MoneyBean {

	private int food_cost;
	private int supplies_expense;
	private int miscellaneous_expenses;
	private int utilities_expense;
	private int rent;

	public int getFood_cost() {
		return food_cost;
	}

	public void setFood_cost(int food_cost) {
		this.food_cost = food_cost;
	}

	public int getSupplies_expense() {
		return supplies_expense;
	}

	public void setSupplies_expense(int supplies_expense) {
		this.supplies_expense = supplies_expense;
	}

	public int getMiscellaneous_expenses() {
		return miscellaneous_expenses;
	}

	public void setMiscellaneous_expenses(int miscellaneous_expenses) {
		this.miscellaneous_expenses = miscellaneous_expenses;
	}

	public int getUtilities_expense() {
		return utilities_expense;
	}

	public void setUtilities_expense(int utilities_expense) {
		this.utilities_expense = utilities_expense;
	}

	public int getRent() {
		return rent;
	}

	public void setRent(int rent) {
		this.rent = rent;
	}

}